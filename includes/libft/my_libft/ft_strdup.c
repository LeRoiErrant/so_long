#include "../libft.h"

char	*ft_strdup(const char *s1)
{
	char		*str;
	size_t		l;
	int			i;

	l = ft_strlen(s1);
	str = malloc((l + 1) * sizeof(*str));
	if (!(str))
		return (0);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
